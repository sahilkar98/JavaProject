package test;
//whether number is positive or negative or zero
public class IfElse {
	public static void main(String[] args) {

		int number = -1200;

		if (number > 0) {
			System.out.println("The number is positive.");
		} else if (number < 0) {
			System.out.println("The number is negative.");
		} else {
			System.out.println("The number is 0.");
		}
	}
}
